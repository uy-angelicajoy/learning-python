# Get First number from user and store into another variable
num1 = int (input("Enter first number: "))
# Get Second number from user and store into another variable
num2 = int (input("Enter second number: "))
# Use if statement to compare the two variables
if num1 == num2:
    # If the variables are equal print "Equal"
    print ("Equal")
else:
    # If the variables are not equal print "Not Equal"
    print ("Not Equal")